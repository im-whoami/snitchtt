/*
 * Simple single-pass XOR obfuscation. Key = 0x42.
 * enc[i] = plaintext[i] ^ SE_KEY
 * dec[i] = enc[i]       ^ SE_KEY  (self-inverse)
 *
 * Strings never appear plaintext in the .so section, defeating naive `strings`
 * scanning. All function symbols are also hidden via -fvisibility=hidden.
 */

#define SE_KEY ((unsigned char)0x42)

static inline char *sd(const unsigned char *e, size_t len) {
    char *o = malloc(len + 1);
    if (!o) return NULL;
    for (size_t i = 0; i < len; i++)
        o[i] = (char)(e[i] ^ SE_KEY);
    o[len] = '\0';
    return o;
}

